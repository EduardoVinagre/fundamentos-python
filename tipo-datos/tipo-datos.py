# Lista
# Se pueden modificar
lista = [0,1,2,3,4,5]
print(lista)

# Tupla
# No se pueden modificar
tupla = (10,20,30,40,50)
print(tupla)

# Diccionario
# Se pueden modificar
diccionario = {"nombre": "Juan", "edad": 30, "ciudad": "Madrid"}
print(diccionario)

# Conjunto (sets)
# Elementos únicos, no ordenados
conjunto = {1, 2, 3, 4, 5}
print(conjunto)

conjunto = {1, 1, 2, 2, 3, 4, 5}
print(conjunto)

# Booleanos
